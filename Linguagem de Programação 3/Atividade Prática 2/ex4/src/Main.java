public class Main {
    public static void main(String[] args) {

        System.out.println(10/4);

        System.out.println(10.0/4);

        /*Quando se divide dois valores inteiros, a divisão será uma divisão inteira,
        ignorando o decimal. Mas quando um dos valores é float/double, será realizado
        uma divisão flutuante, onde o decimal será levado em consideração e o valor
        inteiro será convertido em flutuante antes de calcular*/
    }
}
