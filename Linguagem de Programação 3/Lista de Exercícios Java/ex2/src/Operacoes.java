public class Operacoes {
    public static void main(String[] args) {
        int a = 10;
        int b = 4;

        System.out.println("Divisão inteira = " + (a/b));

        System.out.println("Divisão real = " + ((float)a/b));

        System.out.println("Resto da divisão = " + (a%b));

        double n1 = 7.5;
        double n2 = 8.0;
        double n3 = 4.6;
        System.out.println("Média das notas = " + ((n1+n2+n3)/3));
    }
}
